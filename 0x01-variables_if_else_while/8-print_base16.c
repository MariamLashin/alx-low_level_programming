#include <stdio.h>

/**
 * main - Entry Point
 * Description: prints all the numbers of base 16 in lowercase.
 * Return: always (0) (success)
 */

int main(void)
{
int	digit1 = 48;
	digit2 = 97;

while (digit1 <= 57)
{
	putchar (digit1);
	digit1++;
}
while (digit2 <= 102)
{
	putchar (digit2);
		digit2++;
}
putchar("\n");
return (0);
}

